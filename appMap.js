function n(v) { return Array.isArray(v) ? v.length : (v == null ? 0 : v); }

export function buildAppMap(db = {}) {
  const companies = (db.companies || []).map(c => `${c.name}${c.active ? '' : ' (לא פעיל)'}`).join(', ') || '—';
  const conns = db.connections || {};
  const connLine = ['greenInvoice', 'googleCalendar']
    .map(k => `${k === 'greenInvoice' ? 'חשבונית ירוקה' : 'יומן גוגל'}: ${(conns[k] && conns[k].status) === 'connected' ? 'מחובר' : 'לא מחובר'}`)
    .join(' · ');
  const tabs = TAB_MAP.map(([, label, desc]) => `• ${label} — ${desc}`).join('\n');

  return `# מפת האפליקציה — מערכת א.ש ניהול פיננסי (מעודכן אוטומטית, ${new Date().toLocaleDateString('he-IL')})
מערכת ניהול פיננסי/תפעולי לחברות הגברה ותאורה (הפקת הופעות חיות). בנויה SPA פשוט (HTML/CSS/JS ללא ספריות), עברית ו-RTL, מתארחת ב-Render עם נתונים ב-Postgres.

## עסקים במערכת
${companies}

## אינטגרציות
חשבונית ירוקה (Morning) להפקת מסמכים והוצאות; יומן גוגל לאירועים; גשר ווטסאפ לקליטת אירועים; AI (Claude/Gemini) לקליטת חשבוניות ולצ'אט הצוות.
מצב חיבורים נוכחי: ${connLine}

## הלשוניות והמסכים
${tabs}

## סוגי מסמכים (חשבונית ירוקה)
${DOC_TYPES}

## הרשאות
משתמש הנהלה (admin) רואה הכל. משתמשי צפייה (viewer) רואים רק לשוניות/עסקים מסוימים, בקריאה בלבד.

## שפת העיצוב הקיימת
${DESIGN_SYSTEM}

## שינויים אחרונים (מה השתנה ומה לא)
${CHANGELOG.map(c => '• ' + c).join('\n')}

## נתונים חיים (עכשיו)
אירועים: ${n(db.events)} · לקוחות/ספקים: ${n(db.contractors)} · עובדים: ${n(db.employees)} · חשבוניות שהופקו: ${n(db.invoices)} · תנועות בנק: ${n(db.bankTx)} · בקשות פיתוח פתוחות: ${(db.requests || []).filter(r => r.status !== 'done').length}.

(המפה הזו נבנית מחדש בכל שיחה מתוך הקוד והנתונים החיים, כך שהיא תמיד עדכנית. השתמשי בה כדי להבין את המערכת ולתת המלצות עיצוב קונקרטיות וברות-יישום.)`;
}

